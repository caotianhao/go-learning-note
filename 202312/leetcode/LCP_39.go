package main

import "fmt"

func minimumSwitchingTimes(source [][]int, target [][]int) (sum int) {
	ms := map[int]int{}
	for i := 0; i < len(source); i++ {
		for j := 0; j < len(source[0]); j++ {
			ms[source[i][j]]++
			ms[target[i][j]]--
		}
	}
	for i, v := range ms {
		if v <= 0 {
			delete(ms, i)
		} else {
			sum += v
		}
	}
	return
}

